#include "3-calc.h"
/**
 * op_add - Sums 2 integers
 * @a: input int
 * @b: input int
 * Return: a + b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - main entry function for subtraction
 * @a: first input
 * @b: second input
 * Return: a - b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - main entry multiplier
 * @a: input first
 * @b: input second
 * Return: a * b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - main entry function divider
 * @a: input a
 * @b: input b
 * Return: a / b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - main entry for modulus
 * @a: input first
 * @b: input second
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
